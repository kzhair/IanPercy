                <p>Game-changing ideas are not hard to come by.  They’re in the questions of children, like the daughter of Polaroid founder Edwin Land, who asked, “Daddy, why can’t we see the picture <i>now</i>?”  They’re in daydreams or night dreams, in our musings in the shower or while running.  Someone once asked Johann Sebastian Bach how he got so many musical ideas.  He replied, “It’s all I can do not to step on them in the morning when I get out of bed.”</p>
   <img src="images/quotes/hawking.png" class="quote" alt="Remember to look up at the stars and not down at your feet - Stephen Hawking" />  

				<p>By adulthood, most of us have learned to squelch wild questions and audacious thinking — anything that seems unrealistic.  Yet that is a habit we can all unlearn.  We can once again tap into a world of possibilities as easily as we breathe.</p>
	
				<p>Consider this: As early as 400 BC, Chinese children played with a toy that spun through the air like a helicopter.  In the 1400s, Leonardo da Vinci drew detailed sketches showing how a helicopter would work.  Four hundred years later, French inventor Paul Cornu actually built one.</p>
	
				<p>Likewise, the innovation that will disrupt your industry 50 years from now already exists, if only as a thought.  What’s needed is to take seriously a hope or a dream and bring it into being with courage, wisdom and determination.  You can lead that charge.  <strong>You can spearhead “the next big thing.”</strong>
								</p>

