                <p>In my many years of working with top corporations, I’ve learned two crucial truths: </p>
				                <img src="images/align.jpg" class="floatright" alt="Startup business people discussing disruptive technologies" />

				<ul>
				<li>Anyone, at any level of an organization, is capable of productive possibility thinking.  </li>
				<li>Implementation succeeds only when the organization as a whole nurtures, examines and develops promising ideas rather than squelches them.  </li>
				</ul>
				 <p>For example, I once worked with the sales division of a large insurance company, helping them set their sales strategy for the future.  They chose a 3.5 percent growth rate as their target because that was the general expectation for the industry.  I asked why it couldn’t be 3.7 or 5.3 or even 10 percent.  Sarcastically, one of the guys yelled out that they should go for a billion dollars in sales.  People in the room laughed.</p>

<p>The next day, that same guy came back to the group because something had awakened in him.  He went over, almost day by day, what would need to happen for them to hit the billion-dollar mark.</p>
<p><strong>Suddenly the absurd became totally possible.</strong>

<img src="images/quotes/scully.jpg" class="quote" alt="John Scully quote" /></p>

<p>With that change in mindset, and with new energy, the sales team went at it, supported by the SVP of Sales.  Business began to fall out of the sky.  Smaller firms offered to be acquired.  It was all mind-blowing, and the SVP reminded the team that miracles were happening because that billion-dollar incident changed their thinking. Unfortunately, however, other parts of the insurance company, like IT and Underwriting, did not change their mindset at all.  The company’s seemingly unstoppable breakthrough broke down when the firehose of sales ran into the garden hose of other departments. Leaders had failed to align the whole organization for runaway growth.
</p>

<p>If you Google “possibility thinking” you’ll get 3,770,000 results. You'd think we'd have solved most of the world's problems, wouldn't you? Here’s why we haven't. Dr. Mina Bissell, a brilliant biologist, discovered that a cell's function and biology depend on its surroundings. Since we’re made up of cells, the same is true for how we perform. The secret to seeing and acting on game-changing possibilities depends on our surroundings. Our <i>entire</i> working environment needs to have a "possibility vibe" to it in order to sustain a possibility mindset. Sadly, most organizations don't have it. Let’s work together to ensure a total possibility culture in your organization.
</p>

